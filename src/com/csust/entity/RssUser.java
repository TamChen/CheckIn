package com.csust.entity;

public class RssUser {
	private int id;
	private int userno;
	private String username;
	private int rssid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUserno() {
		return userno;
	}
	public void setUserno(int userno) {
		this.userno = userno;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getRssid() {
		return rssid;
	}
	public void setRssid(int rssid) {
		this.rssid = rssid;
	}



}
